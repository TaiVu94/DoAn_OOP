package models;

/**
 *
 * @author demon
 */
public class Staff {
    int STT;
    String MaNV;
    String TenNV;
    String SDT;
    String DiaChi;
    String MatKhau;

    public Staff(int STT, String MaNV, String TenNV, String DiaChiNV, String SDTNV) {
        this.STT = STT;
        this.MaNV = MaNV;
        this.TenNV = TenNV;
        this.DiaChi = DiaChiNV;
        this.SDT = SDTNV;
    }

    public int getSTT() {
        return STT;
    }

    public void setSTT(int STT) {
        this.STT = STT;
    }

    public String getMaNV() {
        return MaNV;
    }

    public void setMaNV(String MaNV) {
        this.MaNV = MaNV;
    }

    public String getTenNV() {
        return TenNV;
    }

    public void setTenNV(String TenNV) {
        this.TenNV = TenNV;
    }

    public String getSDT() {
        return SDT;
    }

    public void setSDT(String SDT) {
        this.SDT = SDT;
    }

    public String getDiaChi() {
        return DiaChi;
    }

    public void setDiaChi(String DiaChi) {
        this.DiaChi = DiaChi;
    }

    public String getMatKhau() {
        return MatKhau;
    }

    public void setMatKhau(String MatKhau) {
        this.MatKhau = MatKhau;
    }
    
    
}
